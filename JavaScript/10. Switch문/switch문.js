var number = 90;

switch (number) {

    case 100:

        console.log("number는 100입니다.");
        break;

    case 90:

        console.log("number는 90입니다.");
        break;

    case 80:

        console.log("number는 80입니다.");
        break;


    case 70:

        console.log("number는 70입니다.");
        break;

    default:

        console.log("number는 case이외의 값입니다.");
        break;

}