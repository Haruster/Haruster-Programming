# 메서드(Method) : 특정 자료에 대해 특정 기능을 하는 코드를 말한다.
# 함수와 메서드의 차이 : 함수는 특정 기능을 한다(매개변수를 이용해 자료를 전달해준다.) 반면에 메서드는 특정 자료와 연관지어 기능을 한다(자료뒤에 .을 찍어 사용한다.)라는 차이점이 있다.

#메서드의 예 .뒤에 함수가 붙는 경우라고 보면 된다. 즉, 특정 자료에 대해 함수가 역할을 하는 경우

my_list = [1, 2, 3]

my_list.sort() # 변수에 있는 원소를 정렬하는 메서드
my_list.append(5) # 변수에 원소를 추가하는 메서드