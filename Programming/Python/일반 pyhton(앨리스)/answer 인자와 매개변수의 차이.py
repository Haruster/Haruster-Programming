def solve(a, b): #매개변수 a, b를 가진 solve라는 변수를 생성한다.
    return a * b # a와 b를 곱한 값을 반환한다.

var1 = solve(3, 4) #숫자 3과 4를 인자로 넣어서 반환되는 값을 변수 var1에 넣는다.

var2 = solve(3, ["Cham"]) # 숫자 3과 리스트 "Cham"을 인자로 넣어서 반환되는 값을 변수 var2에 넣는다.

print(var1, var2)

#즉, 매개변수는 함수를 만들 때 사용하는 변수이고 인자는 함수를 사용할 때 사용하는 값들을 말한다.